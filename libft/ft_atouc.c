#include "libft.h"

int	ft_atouc(char *str)
{
	int	n;

	n = 0;
	while (ft_isdigit(*str))
	{
		n = n * 10 + *str++ - '0';
		if (n > 255)
			return (-1);
	}
	if (*str)
		return (-1);
	return (n);
}
