#include <header.h>

/* ************************************************************************** */
/*                                                                            */
/*   Multiplie un tuple par un scalaire.									  */
/*                                                                            */
/* ************************************************************************** */
void	t_tuple_multi_scal(t_tuple *tuple, float a)
{
	tuple->x = tuple->x * a;
	tuple->y = tuple->y * a;
	tuple->z = tuple->z * a;
	tuple->w = tuple->w * a;
}
