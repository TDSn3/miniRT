#include <header.h>

/* ************************************************************************** */
/*                                                                            */
/*   Multiplie un tuple par un scalaire.									  */
/*                                                                            */
/* ************************************************************************** */
void	t_tuple_multi_scal(t_tuple *a, t_tuple *left, float right)
{
	a->x = left->x * right;
	a->y = left->y * right;
	a->z = left->z * right;
	a->w = left->w * right;
}
