#include <header.h>

/* ************************************************************************** */
/*                                                                            */
/*   ray[0]   vecteur														  */
/*   ray[1]   point															  */
/*   t        distance														  */
/*                                                                            */
/* ************************************************************************** */
t_tuple	position(t_tuple ray[2], float t)
{
	t_tuple	next_ray;

	init_point(&next_ray, 0, 0, 0);
	t_tuple_multi_scal(&next_ray, &ray[0], t);
	t_tuple_plus(&next_ray, &ray[1], &next_ray);
	return (next_ray);
}
