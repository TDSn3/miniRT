#include <header.h>

/* ************************************************************************** */
/*                                                                            */
/*   rad(π / 2) = 90														  */
/*   rad(π)     = 180														  */
/*   rad(π * 2) = 360														  */
/*                                                                            */
/* ************************************************************************** */
float	deg_to_rad(float deg)
{
	return ((M_PI / 180.0) * deg);
}

float	rad_to_deg(float rad)
{
	return ((180.0 * rad) / M_PI);
}
