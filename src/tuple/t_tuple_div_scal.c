#include <header.h>

/* ************************************************************************** */
/*                                                                            */
/*   Divise un tuple par un scalaire.										  */
/*                                                                            */
/* ************************************************************************** */
void	t_tuple_div_scal(t_tuple *a, t_tuple *left, double right)
{
	a->x = left->x / right;
	a->y = left->y / right;
	a->z = left->z / right;
	a->w = left->w / right;
}
