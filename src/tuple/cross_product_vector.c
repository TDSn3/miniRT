t_tuple	cross_product_vector(t_tuple const *a, t_tuple const *b)
{
	assert(a->w == 0 && b->w == 0);
	return ((t_tuple){
		.x = a->y * b->z - a->z * b->y,
		.y = a->z * b->x - a->x * b->z,
		.z = a->x * b->y - a->y * b->x,
		.w = 0});
}
