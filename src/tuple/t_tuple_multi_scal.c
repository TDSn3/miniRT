/* ************************************************************************** */
/*                                                                            */
/*   Multiplie un tuple par un scalaire.									  */
/*                                                                            */
/* ************************************************************************** */
t_tuple	t_tuple_scale(t_tuple const *left, double right)
{
	return ((t_tuple){{
			left->x * right,
			left->y * right,
			left->z * right,
			left->w * right}});
}
